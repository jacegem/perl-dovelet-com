제한시간: 1 초
n 명이 원탁에 앉아 tea party 를 하고 있다.

1 분 마다 인접한 자리에 앉은 오직 한 쌍이 서로 자리를 바꿀 수 있는 경우 완전히 거꾸로( 왼쪽사람이 오른쪽 , 오른쪽 사람이 왼쪽 ) 할 수 있는 최소 시간( 분) 을 출력하는게 문제이다.



입력

파티 참석자 수 n 이 ( 1 <= n <= 32767 ) 이 입력으로 주어진다.
출력

최소 시간(분)을 출력한다.
입출력 예

입력

4

출력

2

입력

5

출력

4

입력

2

출력

0
